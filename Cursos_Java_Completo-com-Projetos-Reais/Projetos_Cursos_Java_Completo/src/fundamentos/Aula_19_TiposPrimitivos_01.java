package fundamentos;

public class Aula_19_TiposPrimitivos_01 {
	// exitem 8 tipos primitivos em java, são: 
	//1° byte  -> armazenar 8 bits -128 até +127
	//2° Short -> armazenar 16 bits, ou seja,  entre -32.768 e 32.767.
	//3° Int   ->  armazenar 32 bits, entre -2.147.483.648 e 2.147.483.647.
	//4° Long  ->  armazenar 64 bits, entre -9.223.372.036.854.775.808L e 9.223.372.036.854.775.807L
	//5° Float ->  armazenar números reais 32 bits.
	//6° double -> armazenar números reais de precisão dupla,  64 bits uma precisão maior que a de float.
	//7° Char   -> armazenar 16 bits representando caracteres no formato UTF-16.
	//8° Boolean -> É o tipo de dado que contém literal lógico. Serve para armazenar um único bit de informação. Este bit pode ser representado pelas palavras false (falso) ou true (verdadeiro).
}
