package a_05_orientação_A_Objetos;

public class Aula_139_Herança {
	//A herança é um mecanismo da Orientação a Objeto que permite criar novas 
	//classes a partir de classes já existentes, aproveitando-se das características 
	//existentes na classe a ser estendida. Este mecanismo é muito interessante,
	//pois promove um grande reuso e reaproveitamento de código existente. 
	//Com a herança é possível criar classes derivadas, subclasses, a partir de classes bases, 
	//superclasses. As subclasses são mais especializadas do que as suas superclasses, mais genéricas.
	//As subclasses herdam todas as características de suas superclasses, como suas 
	//variáveis e métodos. A linguagem Java permite o uso de herança simples, mas não 
	//permite a implementação de herança múltipla. Para superar essa limitação o Java 
	//faz uso de interfaces, o qual pode ser visto como uma “promessa”que certos métodos 
	//com características previamente estabelecidas serão implementados, usando inclusive a palavra
	//reservada implements para garantir esta implementação. As interfaces possuem sintaxe similar
	//as classes, no entanto apresentam apenas a especificação das funcionalidades que uma classe 
	//deve conter, sem determinar como essa funcionalidade deve ser implementada. 
	//Apresentam apenas protótipos dos métodos.

}
